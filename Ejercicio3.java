package ejercicios;

public class Ejercicio3 {
	
	public static void main(String[] args) {
		System.out.println("Tecla de escape\tSignificado");
		System.out.println("---------------\t-------------------------------\n");
		System.out.println("\\n\t\tSalto de linea");
		System.out.println("\\t\t\tTabulación");
		System.out.println("\\\"\t\tPara poner \" (comillas dobles dentro de texto)"
				+ ", por ejemplo \"Oscar\"");
		System.out.println("\\\\\t\tEscribir \\ dentro de texto, por ejemplo "
				+ "\\ejemplo\\");
		System.out.println("\\'\t\tCuando queremos usar \' (comilla simple), como por "
				+ "ejemplo \'Dijosorprendido\'");
	
	}
	
}
